        public void autoID() throws SQLException{
         try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            con=DriverManager.getConnection("jdbc:mysql://localhost/airline","root","");
            
            Statement s =con.createStatement();
            ResultSet rs=s.executeQuery("select MAX(FlightID) from flight");
            rs.next();
            rs.getString("MAX(FlightID)");
            
            if(rs.getString("MAX(FlightID)")==null){
              fID.setText("111");
              
            }
            else{
               long id =Long.parseLong(rs.getString("MAX(FlightID)"));
               id++;
               fID.setText(String.format("%03d", id));
    
            }

        }
        catch (ClassNotFoundException ex) {
            Logger.getLogger(NewFlight.class.getName()).log(Level.SEVERE, null, ex);
        }
        }
