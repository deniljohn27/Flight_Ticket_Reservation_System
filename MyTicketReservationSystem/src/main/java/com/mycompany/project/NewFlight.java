    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String id=fID.getText();
        String source= flightSource.getSelectedItem().toString();
        String destination=flightDestination.getSelectedItem().toString();
        String departure=flightDeparture.getText();
        String arrival=flightArrival.getText();
        String name=flightName.getText();
        
        DateFormat dt=new SimpleDateFormat("yyyy-MM-dd");
        String date=dt.format(flightDate.getDate());
        
       String price=flightPrice.getText();
        
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            con=DriverManager.getConnection("jdbc:mysql://localhost/airline","root","");
            pt = con.prepareStatement("insert into flight(FlightID,FlightName,FlightSource,FlightDestination,FlightDeparture,FlightArrival,FlightDate,FlightPrice)values(?,?,?,?,?,?,?,?)");
            pt.setString(1,id);
            pt.setString(2,name);
            pt.setString(3,source);
            pt.setString(4,destination);
            pt.setString(5,departure);
            pt.setString(6,arrival);
            pt.setString(7,date);
            pt.setString(8,price);
           
            pt.executeUpdate();
           JOptionPane.showMessageDialog(null,"Records UPdates Successfully");

            
            
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(NewAddCustomer1.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(NewAddCustomer1.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_jButton1ActionPerformed

