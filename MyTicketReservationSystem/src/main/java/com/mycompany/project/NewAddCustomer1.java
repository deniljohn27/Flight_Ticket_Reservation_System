    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String id=txtid.getText();
        String firstName=FirstName.getText();
        String lastName=LastName.getText();
        String aadhar=Aadhar.getText();
        String passport=Passport.getText();
        String address=Address.getText();
        String emailID=EmailID.getText();

        DateFormat dt=new SimpleDateFormat("yyyy-MM-dd");
        String date=dt.format(DOB.getDate());

        String gender;
        if(r1.isSelected()){
            gender="Male";

        }else{
            gender="Female";}

        String contact=Contact.getText();

        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            con=DriverManager.getConnection("jdbc:mysql://localhost/airline","root","");
            pt = con.prepareStatement("insert into customer(customerID,FirstName,LastName,Aadhar,Passport,Address,DOB,Gender,Contact,EmailID)values(?,?,?,?,?,?,?,?,?,?)");
            pt.setString(1,id);
            pt.setString(2,firstName);
            pt.setString(3,lastName);
            pt.setString(4,aadhar);
            pt.setString(5,passport);
            pt.setString(6,address);
            pt.setString(7,date);
            pt.setString(8,gender);
            pt.setString(9,contact);
            pt.setString(10,emailID);

            pt.executeUpdate();
            JOptionPane.showMessageDialog(null,"Records UPdates Successfully");

        } catch (ClassNotFoundException ex) {
            Logger.getLogger(NewAddCustomer1.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(NewAddCustomer1.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

