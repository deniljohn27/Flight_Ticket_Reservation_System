            public void autoID() throws SQLException{
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            con=DriverManager.getConnection("jdbc:mysql://localhost/airline","root","");
            
            Statement s =con.createStatement();
            ResultSet rs=s.executeQuery("select MAX(customerId) from customer");
            rs.next();
            rs.getString("MAX(customerID)");
            
            if(rs.getString("MAX(customerID)")==null){
              txtid.setText("001");
              
            }
            else{
               long id =Long.parseLong(rs.getString("MAX(customerID)"));
               id++;
               txtid.setText(String.format("%03d", id));
               
                
            }

        } catch (ClassNotFoundException ex) {
            Logger.getLogger(NewAddCustomer1.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
